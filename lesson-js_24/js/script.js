// Відповідь:

function calcDiv (a, b) {
	let result = a / b;
	if (typeof a !== "number" || typeof b !== "number") {
		console.log("Введіть ДВА ЧИСЛА, а не оце все!");
	} else if (b == 0) {
	console.log("На нуль ділити не можна");
	} else {
		console.log(`Результат ділення: ${result}`);
	}
};
